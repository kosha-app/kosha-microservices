package com.sage.sage.microservices.coroutines

class TheFileOfLegends {

    fun addition(a: Int, b:Int): Int{
        return a + b
    }

    fun subtraction(a: Int, b:Int): Int{
        return a - b
    }

    fun multiplication(a: Int, b:Int): Int{
        return a * b
    }

    fun division(a: Int, b:Int): Int{
        return a / b
    }
}